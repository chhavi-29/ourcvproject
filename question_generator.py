import random

class QuestionGenerator:
    def __init__(self):
        self.easy_questions = [
            ("5 + 3 = ?", 8, [6, 9, 10]),
            ("12 - 4 = ?", 8, [6, 9, 10]),
            ("7 + 6 = ?", 13, [11, 14, 15]),
            ("15 - 8 = ?", 7, [5, 9, 6]),
            ("9 + 4 = ?", 13, [12, 11, 14]),
            ("20 - 11 = ?", 9, [7, 8, 11]),
            ("14 + 5 = ?", 19, [17, 18, 20]),
            ("18 - 9 = ?", 9, [7, 8, 10]),
            ("8 + 8 = ?", 16, [14, 15, 18]),
            ("25 - 10 = ?", 15, [12, 14, 16]),
            ("11 + 7 = ?", 18, [16, 17, 19]),
            ("22 - 7 = ?", 15, [13, 14, 17])
        ]
        self.medium_questions = [
            ("6 x 7 = ?", 42, [36, 48, 54]),
            ("8 x 4 = ?", 32, [24, 28, 36]),
            ("5 x 9 = ?", 45, [35, 40, 50]),
            ("12 x 3 = ?", 36, [24, 30, 42]),
            ("48 ÷ 6 = ?", 8, [6, 7, 9]),
            ("7 x 8 = ?", 56, [48, 62, 64]),
            ("35 ÷ 5 = ?", 7, [6, 8, 9]),
            ("9 x 6 = ?", 54, [45, 48, 60]),
            ("72 ÷ 9 = ?", 8, [7, 9, 10]),
            ("11 x 4 = ?", 44, [33, 40, 48]),
            ("81 ÷ 9 = ?", 9, [7, 8, 10]),
            ("6 x 6 = ?", 36, [24, 30, 42])
        ]
        self.hard_questions = [
            ("7² = ?", 49, [42, 48, 56]),
            ("8² = ?", 64, [56, 72, 81]),
            ("9² = ?", 81, [72, 90, 84]),
            ("5² + 4 = ?", 29, [25, 27, 31]),
            ("6² - 6 = ?", 30, [24, 32, 36]),
            ("11² = ?", 121, [111, 100, 132]),
            ("4² + 3² = ?", 25, [16, 21, 32]),
            ("10² - 15 = ?", 85, [75, 90, 95]),
            ("12² = ?", 144, [124, 132, 154]),
            ("8 x 9 + 4 = ?", 76, [72, 68, 80]),
            ("7 x 6 - 5 = ?", 37, [35, 42, 45]),
            ("9 x 8 - 12 = ?", 60, [50, 64, 70])
        ]
    
    def generate(self, difficulty):
        if difficulty == "easy":
            return random.choice(self.easy_questions)
        elif difficulty == "medium":
            return random.choice(self.medium_questions)
        else:
            return random.choice(self.hard_questions)

if __name__ == "__main__":
    qg = QuestionGenerator()
    print("Easy:", qg.generate("easy"))
    print("Medium:", qg.generate("medium"))
    print("Hard:", qg.generate("hard"))
